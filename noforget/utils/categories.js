function pickSubtitle(categoryId) {
  const cat = categories.find(c => c.id === categoryId)
  if (!cat || !cat.subtitles || cat.subtitles.length === 0) return ''
  const idx = Math.floor(Math.random() * cat.subtitles.length)
  return cat.subtitles[idx]
}

function getCategoryById(id) {
  return categories.find(c => c.id === id) || categories[0]
}

function getAllCategories() {
  return categories
}

function isRecurringCategory(categoryId) {
  return RECURRING_CATEGORIES.has(categoryId)
}

